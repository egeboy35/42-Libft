#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	i;
	size_t	q;

	if (!s1 || !set)
		return (NULL);
	i = 0;
	while (ft_strchr(set, s1[i]) && s1[i] != '\0')
		i++;
	q = ft_strlen((char *)s1);
	while (ft_strchr(set, s1[q]) && q != 0)
		q--;
	return (ft_substr(s1, i, (q - i + 1)));
}
